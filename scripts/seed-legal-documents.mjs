const LEGAL_DOCUMENTS = [
  {
    documentType: "DATA_PROCESSING_AGREEMENT",
    title: "ข้อตกลงการประมวลผลข้อมูลส่วนบุคคล (PDPA/GDPR Consent) v1.0",
    version: "1.0",
    content: PDPA_CONTENT,
    requiredForFeatures: ["ORDER_MANAGEMENT"],
  },
  {
    documentType: "TENANT_TERMS_OF_SERVICE",
    title: "ข้อกำหนดและเงื่อนไขการใช้บริการ (Terms & Conditions) v1.0",
    version: "1.0",
    content: TNC_CONTENT,
    requiredForFeatures: ["ORDER_MANAGEMENT"],
  },
];

async function main() {
  const { client, mode } = await connectMongoClient();
  console.log(`✅ Connected (${mode})\n`);
  try {
    const col = client.db(process.env.MONGO_DB_NAME || "zudobot_saas").collection("legaldocuments");
    const now = new Date();
    for (const doc of LEGAL_DOCUMENTS) {
      const existing = await col.findOne({ documentType: doc.documentType, status: "ACTIVE" });
      if (existing) {
        console.log(`⏭  ACTIVE ${doc.documentType} already exists — skipping.`);
        continue;
      }
      const r = await col.insertOne({
        _id: new ObjectId(),
        documentType: doc.documentType,
        title: doc.title,
        version: doc.version,
        content: doc.content,
        status: "ACTIVE",
        effectiveAt: now,
        requiredForFeatures: doc.requiredForFeatures,
        createdAt: now,
        updatedAt: now,
      });
      console.log(`✅ Inserted ACTIVE ${doc.documentType} → ${r.insertedId}`);
    }
    console.log("\n✅ Seed complete.");
  } finally {
    await client.close();
  }
}

main().catch((err) => {
  console.error("❌ Seed failed:", err);
  process.exit(1);
});
